public class VrfFactory {

    public static List<IVrfHandler> getVrfHandlerChain (VrfInput input) {
        List<IVrfHandler> vrfHandlerChain = new ArrayList<>();
        VrfEntry vrfEntry = input.getVrfEntry();
        /*
         * Check if its a subnet route VRF entry.
         */
        SubnetRoute subnetRoute = vrfEntry.getAugmentation(SubnetRoute.class);
        if (subnetRoute != null) {
            vrfHandlerChain.add(new SubnetRouteVrfHandler(input));
        }

        /*
         * Check if its a self_imported route VRF entry.
         */
        if (RouteOrigin.value(vrfEntry.getOrigin()) == RouteOrigin.SELF_IMPORTED) {
            vrfHandlerChain.add(new ImportExportVrfHandler(input));
        }

        return vrfHandlerChain;
    }
}